  export function generateBlock(level: number, index: number): Block[] {
    const array: Block[] = [];
    let sCount = 0;
    let cCount = 0;
  
    let maxS = 1;
    if (level === 1) {
      maxS = 2;
    } else if (level === 0) {
      maxS = 3;
    }
  
    for (let i = 0; i < 4; i++) {
      if ((sCount < maxS && cCount < 4 - maxS) || cCount === 4 - maxS) {
        array.push({ id: 0, type: "skull", isShow: false });
        sCount++;
      } else {
        array.push({ id: 0, type: "coin", isShow: false });
        cCount++;
      }
    }
  
    array.sort(() => Math.random() - 0.5);
    array.forEach((_item, i) => {
      array[i].id = 4 * index + i + 1;
    });
    return array;
  }
  